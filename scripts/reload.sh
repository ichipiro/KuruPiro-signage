#!/bin/bash
set -euo pipefail

# ==============================================================================
# reload.sh - Chromium をリロード（F5キー送信）
# ==============================================================================
# 2時間ごとに systemd timer から呼び出されます。
# ==============================================================================

# ディスプレイ番号は環境に応じて調整（基本 :0 でOK）
DISPLAY=:0 xdotool key F5 || echo "[kurupiro] xdotool F5 失敗" >&2
